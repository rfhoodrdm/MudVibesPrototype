package com.example.mudvibe.common.exception;

public class PlayerCharacterLoadDataException extends Exception {

	private static final long serialVersionUID = 5970087787310701757L;

	public PlayerCharacterLoadDataException() {
		super();
	}

	public PlayerCharacterLoadDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public PlayerCharacterLoadDataException(String message) {
		super(message);
	}

	public PlayerCharacterLoadDataException(Throwable cause) {
		super(cause);
	}
}
